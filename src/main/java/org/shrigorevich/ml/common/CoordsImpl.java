package org.shrigorevich.ml.common;

public class CoordsImpl implements Coords {

    private final int x, y, z;

    public CoordsImpl(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    @Override
    public int getX() {
        return x;
    }

    @Override
    public int getY() {
        return y;
    }

    @Override
    public int getZ() {
        return z;
    }
}
